    private void botonCamionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCamionActionPerformed
        // TODO add your handling code here:
        //creamos una variable para que ingrese la matricula
        String  placa = JOptionPane.showInputDialog(null, "Ingrese la matricula del camion: ");
        //Verificamos si el usuario ingresa un valor no nulo
        if(placa != null && !placa.trim().isEmpty()){
            //Creamos una variable para que ingrese los ejes 
            String ejesStr = JOptionPane.showInputDialog(null, "Ingrese el numero de ejes: ");
            try{
                //Creamos una variable que guarde el valor entero de ejesStr
                int ejes = Integer.parseInt(ejesStr);
                //A単adimos el vehiculo usando la matricula ingresada y los ejes ingresados
                peaje.a単adirVehiculo(new Camion(placa.trim(), ejes));
                //Mostramos el total de peaje acumulado
                totalPeaje.setText("Total peaje: " + peaje.getTotalPeaje());
                
            }catch(NumberFormatException ex){
                
                //Si hay un error presentamos un mensaje 
                JOptionPane.showMessageDialog(null, "Numero de ejes invalidos");
                
            }
        }
        
    }//GEN-LAST:event_botonCamionActionPerformed

